            Personal personal = new Personal();
            personal.setId(id);
            Aluno c = new Aluno();
            AlunoDAO dao = new AlunoDAO();
            c.setId((int) jCadastroAlunos.getValueAt(jCadastroAlunos.getSelectedRow(), 6));
            dao.delete(c);

            txtNome.setText("");
            txtCPF.setText("");
