    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jCadastroAlunos.getModel();
        modelo.setNumRows(0);
        AlunoDAO pdao = new AlunoDAO();

        for (Aluno a : pdao.read(id)) {

            modelo.addRow(new Object[]{
                a.getNome(),
                a.getCpf(),
                a.getEmail(),
                a.getCep(),
                a.getCelular(),
                a.getEndereco(),
                a.getId()

            });

        }

    }

